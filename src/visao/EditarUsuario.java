    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String senha = String.valueOf(jnovasenha.getPassword());
        String repete = String.valueOf(jrepete.getPassword());
        try {
            if(senha.equals(repete)){
                this.usuario.setLogin(jlogin.getText());
                this.usuario.setEmail(jemail.getText());
                this.usuario.setSenha(String.valueOf(jnovasenha.getPassword()));
                this.usuario.setNome(jnome.getText());

                FachadaControle.getFachadaControle().editarUsuario(usuario, String.valueOf(jsenha.getPassword()));
                JOptionPane.showMessageDialog(null, "Usuário alterado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "Repita a senha corretamente!");
            }
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

