    private void jvalorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jvalorKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            this.abrirCaixa();
        }else{
            if(evt.getKeyCode() == KeyEvent.VK_ESCAPE){
                this.dispose();
            }
        }
    }//GEN-LAST:event_jvalorKeyPressed

    public void abrirCaixa(){
        try {
            FachadaControle.getFachadaControle().abrirCaixa(Double.parseDouble(jvalor.getText().replace(',', '.')));
            JOptionPane.showMessageDialog(null, "Caixa aberto com sucesso!");
            
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            if(ex.getMessage().contains("For")){
                JOptionPane.showMessageDialog(null, "Digite um valor v√°lido!");
            }else{
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }
