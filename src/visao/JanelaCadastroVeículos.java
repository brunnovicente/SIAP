    private void pegarDados(){
        try{
        int num = Integer.parseInt(jano.getText());
        veiculo.setPlaca(jplaca.getText().toUpperCase());
        veiculo.setModelo(jmodelo.getText().toUpperCase());
        veiculo.setCor(jcor.getText().toUpperCase());
        veiculo.setAno(Integer.parseInt(jano.getText()));
        this.dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Valor inválido para o ano!");
        }
    }
    
