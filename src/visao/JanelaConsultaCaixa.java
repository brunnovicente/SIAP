    public void buscarCaixas(String inicio, String fim){
        //String dataInicial = ((JCalendar)jdataInicial).getText();
        //String dataFinal = ((JCalendar)jdataFinal).getText();
        
        this.lista = FachadaControle.getFachadaControle().buscarCaixas(inicio, fim);
        
            DefaultTableModel modelo = (DefaultTableModel) jtabela.getModel();
            //Limpa a tabela antes de adicionar os novos dados
            int tam = modelo.getRowCount();
            for(int i=0;i<tam;i++){
                modelo.removeRow(0);
            }
            
            java.text.SimpleDateFormat formatodata = new java.text.SimpleDateFormat("dd/MM/yyyy");  
            
            for(Caixa caixa : this.lista){
                String[] linha = new String[7];
                linha[0] = caixa.getCodigo()+"";
                linha[1] = "R$ "+(caixa.getCaixaInicial()+"").replace(".", ",");
                linha[2] = "R$ "+(caixa.getCaixaFinal()+"").replace(".", ",");
                linha[3] = "R$ "+(caixa.getTotalDinheiro()+"").replace(".", ",");
                linha[4] = "R$ "+(caixa.getTotalCartao()+"").replace(".", ",");
                linha[5] = formatodata.format(caixa.getDataInicial());
                linha[6] = caixa.getStatus();
                modelo.addRow(linha);
            }
        
    }
    
