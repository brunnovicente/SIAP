    private void jbutaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutaoExcluirActionPerformed
        int valor = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?", "Excluir Produto", JOptionPane.YES_NO_OPTION);
        if(valor == JOptionPane.YES_OPTION){
            int i = jTabelaPesquisa.getSelectedRow();
            Produto p = this.lista.get(i);
            
            p.setStatus("Inativo");

            try {
                //this.banco.excluirProduto(p);
                FachadaControle.getFachadaControle().editarProduto(p);
                //FachadaControle.getFachadaControle().Fechar();
                //this.lista = new ArrayList<>();
                //FachadaControle.getFachadaControle().Abrir();
                
                this.buscarProduto();

                for (Produto pro : lista) {
                    JOptionPane.showMessageDialog(null, pro.getDescricao()+" - "+pro.getStatus());
                }
            } catch (SQLException ex) {
                Logger.getLogger(JanelaConsultaProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jbutaoExcluirActionPerformed

