    private void produtoSelecionado(int i){
        //int i = this.jTabelaPesquisa.getSelectedRow();
        //Produto pro = this.lista.get(i);
        String codigo = (String) jTabelaPesquisa.getValueAt(i, 0);
        
        
        jfoto.setIcon(null);
        //byte[] fotodados = produto.getFoto();
        try{
            Produto pro = FachadaControle.getFachadaControle().buscaProdutoBarra(codigo);
//            FileOutputStream fos = new FileOutputStream(produto.getNomefoto()+".jpg"); 
//            fos.write(fotodados);
//            fos.close();
            
            //Exibindo a imagem no JLabelprod
           jfoto.setIcon(null);
           ImageIcon img = new ImageIcon(pro.getFoto());
           Image foto = img.getImage().getScaledInstance(jfoto.getWidth(), jfoto.getHeight(), Image.SCALE_DEFAULT);
           jfoto.setIcon(new ImageIcon(foto));
           
           DefaultTableModel modelo = (DefaultTableModel) jtabelasimilar.getModel();
           int j = jtabelasimilar.getRowCount();
            for (int k = 0; k < j; k++) {
                modelo.removeRow(0);
            }
           
           for (Produto similar : pro.getSimilares()) {
               if(similar.getStatus().equals(Produto.ATIVO)){
                    String[] linha = new String[5];
                    linha[0] = similar.getCodigoproduto();
                    linha[1] = similar.getDescricao();
                    linha[2] = "R$ "+similar.getPrecoVenda()+"";
                    linha[3] = similar.getEstoqueAtual()+"";
                    linha[4] = similar.getFabricante().getNome();
                    modelo.addRow(linha);
               }
           }
           
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
