    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            int codigo = Integer.parseInt((String)jTabelaPesquisa.getValueAt(jTabelaPesquisa.getSelectedRow(), 0));
            Pedido pedido = FachadaControle.getFachadaControle().getPedido(codigo);
            if(!pedido.getStatus().equals(Pedido.CANCELADO)){
                pedido.setStatus(Pedido.CONCLUIDO);
                FachadaControle.getFachadaControle().modificarStatus(pedido);
                this.buscaPedido();
                JOptionPane.showMessageDialog(null, "Pedido recebido com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "O pedido já foi cancelado!");
            }
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Selecione um pedido!");
        } catch (SQLException ex) {
            Logger.getLogger(JanelaConsultaPedidos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

