    public void selecionarItem(){
        int sel = jTabelaPesquisa.getSelectedRow();
        Pedido pedido = lista.get(sel);
                
        DefaultTableModel model = (DefaultTableModel) jtabelaItens.getModel();
        //Limpa a tabela antes de adicionar os novos dados
        int tam = model.getRowCount();
        for(int i=0;i<tam;i++){
            model.removeRow(0);
        }
        
        DateFormat df = new SimpleDateFormat("dd/mm/yyyy");
        String dataPed = df.format(pedido.getDataPedido());
        String dataPrev = df.format(pedido.getDataPrevista());
        jdataPedido.setText(dataPed);
        jdataPrevista.setText(dataPrev);
        jtotalPedido.setText("R$ "+pedido.getTotal());
        
        for (ItensPedido item : pedido.getItens()) {
            String[] linha = new String[4];
            linha[0] = item.getProduto().getDescricao();
            linha[1] = "R$ "+item.getPreco();
            linha[2] = item.getQuantidade()+"";
            linha[3] = "R$ "+item.getTotal();
            model.addRow(linha);
        }
    }
    
    public void buscaPedido(){
        
        DefaultTableModel model1 = (DefaultTableModel) jtabelaItens.getModel();
        //Limpa a tabela antes de adicionar os novos dados
        int tam = model1.getRowCount();
        for(int i=0;i<tam;i++){
            model1.removeRow(0);
        }
        
        jdataPedido.setText("");
        jdataPrevista.setText("");
        jtotalPedido.setText("");
        
        DefaultTableModel model = (DefaultTableModel) jTabelaPesquisa.getModel();
        //Limpa a tabela antes de adicionar os novos dados
        tam = model.getRowCount();
        for(int i=0;i<tam;i++){
            model.removeRow(0);
        }
        
        
        try {
            if(jtodos.isSelected()){
                lista = FachadaControle.getFachadaControle().consultaPedidos();
            }else if(jconcluidos.isSelected()){
                lista = FachadaControle.getFachadaControle().consultaPedidosPorStatus(Pedido.CONCLUIDO);
            }else if(jpendentes.isSelected()){
                lista = FachadaControle.getFachadaControle().consultaPedidosPorStatus(Pedido.PENDENTE);
            }else if(jcancelados.isSelected()){
                lista = FachadaControle.getFachadaControle().consultaPedidosPorStatus(Pedido.CANCELADO);
            }else{
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                lista = FachadaControle.getFachadaControle().consultaPedidosAtrasados(sdf.format(new Date()));
            }
            
            for(Pedido pedido : lista){
                String[] linha = new String[9];
                linha[0] = pedido.getCodigo()+"";
                linha[1] = pedido.getFornecedor().getNome();
                linha[2] = pedido.getStatus();
                
                model.addRow(linha);
            }
            
            
            
            jTabelaPesquisa.requestFocus();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(JanelaConsultaPedidos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(JanelaConsultaPedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
    
    
    
    
    
