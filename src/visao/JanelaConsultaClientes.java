    public void adicionar(){
        int i = jtabelaclientes.getSelectedRow();
        int j = jtabelacarros.getSelectedRow();
        if(j > -1 && i > -1){
            Cliente c = this.lista.get(i);
            Veiculo v = c.getVeiculos().get(j);

    //        c.setVeiculos(new ArrayList<Veiculo>());
    //        c.getVeiculos().add(v);

            cliente.setCodigo(c.getCodigo());
            cliente.setNome(c.getNome());
            cliente.setApelido(c.getApelido());
            cliente.setBairro(c.getBairro());
            cliente.setCelular(c.getCelular());
            cliente.setTelefone(c.getTelefone());
            cliente.setCep(c.getCep());
            cliente.setCpf(c.getCpf());
            cliente.setRg(c.getRg());
            cliente.setEndereco(c.getEndereco());
            cliente.setNumero(c.getNumero());
            cliente.setObservacao(c.getObservacao());
            cliente.setStatus(c.getStatus());
            cliente.setSexo(c.getSexo());

            cliente.setVeiculos(new ArrayList<Veiculo>());
            cliente.getVeiculos().add(v);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um Cliente e um Veículo!");
        }
    }
    
    
    public void excluir(int codigo) throws Exception{
        if(codigo == 1)
            throw new Exception("Cliente PADRÃO não pode ser excluído");
        
        FachadaControle.getFachadaControle().excluirCliente(codigo);
        DefaultTableModel modelo = (DefaultTableModel) jtabelaclientes.getModel();
        DefaultTableModel modelocarros = (DefaultTableModel) jtabelacarros.getModel();
            //Limpa a tabela antes de adicionar os novos dados
        int c = jtabelaclientes.getSelectedRow();
        modelo.removeRow(c);
            
        int t = modelocarros.getRowCount();
        for(int i=0;i<t;i++){
            modelocarros.removeRow(0);
        }
    }
    
    public void consultar(){
        try {
            lista = FachadaControle.getFachadaControle().consultaClientes(jChave.getText());
            this.meteNaTabela();
        } catch (Exception ex) {
            Logger.getLogger(JanelaConsultaClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    public void consultarPlaca(){
        try {
            Veiculo veiculo = FachadaControle.getFachadaControle().getVeiculo(jplaca.getText());
            
            if(veiculo != null){
                this.lista = FachadaControle.getFachadaControle().consultaClientes(veiculo);
                this.meteNaTabela();
            }else{
                JOptionPane.showMessageDialog(null, "Veículo de placa '"+jplaca.getText()+"' não encontrado!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(JanelaConsultaClientes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    private void meteNaTabela(){
        DefaultTableModel modelo = (DefaultTableModel) jtabelaclientes.getModel();
        DefaultTableModel modelocarros = (DefaultTableModel) jtabelacarros.getModel();
            //Limpa a tabela antes de adicionar os novos dados
            int tam = modelo.getRowCount();
            for(int i=0;i<tam;i++){
                modelo.removeRow(0);
            }
            
            tam = modelocarros.getRowCount();
            for(int i=0;i<tam;i++){
                modelocarros.removeRow(0);
            }
            
            for (Cliente cliente : lista) {
                String[] linha = new String[3];
                linha[0] = cliente.getCodigo()+"";
                linha[1] = cliente.getNome();
                linha[2] = cliente.getApelido();
                modelo.addRow(linha);
    
            }
                       
    }
    
    private void adicionarTabelaVeiculos(Cliente cliente){
        DefaultTableModel modelo = (DefaultTableModel) jtabelacarros.getModel();
        
            //Limpa a tabela antes de adicionar os novos dados
            int tam = modelo.getRowCount();
            for(int i=0;i<tam;i++){
                modelo.removeRow(0);
            }
                String[] linha = new String[3];
                for (Veiculo veiculo : cliente.getVeiculos()) {
                    linha[0] = veiculo.getPlaca();
                    linha[1] = veiculo.getModelo();
                    linha[2] = veiculo.getAno()+"";
                    modelo.addRow(linha);
                }
                
            
    }
    
    
    
    
