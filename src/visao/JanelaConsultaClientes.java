    private void jbutaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutaoExcluirActionPerformed
        int sim = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o cliente?", "Exclusão", JOptionPane.YES_NO_OPTION);
        if(sim == JOptionPane.YES_OPTION){
            int i = jtabelaclientes.getSelectedRow();
            int codigo = this.lista.get(i).getCodigo();


            try {
                this.excluir(codigo);
                JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jbutaoExcluirActionPerformed

